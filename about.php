        <h1>About Blood Donation</h1><hr>
        <h4>Facts about blood needs</h4><hr>
         
     <ul>         
<li>Every year our nation requires about 4 Crore units of blood, out of which only a meager 40 Lakh units of blood are available.</li>
<li>The gift of blood is the gift of life. There is no substitute for human blood.</li>
<li>Every two seconds someone needs blood.</li> 
<li>More than 38,000 blood donations are needed every day.</li>
<li>A total of 30 million blood components are transfused each year.<img src="images_imageMenu/blood_3.jpg" align="right"></li> 
<li>The average red blood cell transfusion is approximately 3 pints.</li>
<li>The blood type most often requested by hospitals is Type O.</li>
<li>Sickle cell patients can require frequent blood transfusions throughout their lives.</li>
<li>More than 1 million new people are diagnosed with cancer each year. Many of them will need blood, sometimes daily, during their chemotherapy treatment.</li>
<li>A single car accident victim can require as many as 100 units of blood</li>
</ul><hr>

<h4>Facts about the blood supply</h4>      
     <ul>         
<li>Blood cannot be manufactured – it can only come from generous donors.<img src="images_imageMenu/blood_4.jpg" align="right"></li>
<li>Type O-negative blood (red cells) can be transfused to patients of all blood types. It is always in great demand and often in short supply.</li>
<li>Type AB-positive plasma can be transfused to patients of all other blood types. AB plasma is also usually in short supply.</li> 
</ul><hr>

<h4>Facts about the blood donation process</h4>   
     <ul>         
<li>Donating blood is a safe process. A sterile needle is used only once for each donor and then discarded.<img src="images_imageMenu/blood_1.jpg" align="right"></li>
<li>Blood donation is a simple four-step process: registration, medical history and mini-physical, donation and refreshments.</li>
<li>Every blood donor is given a mini-physical, checking the donor's temperature, blood pressure, pulse and hemoglobin to ensure it is safe for the donor to give blood.</li> 
<li>The actual blood donation typically takes less than 10-12 minutes. The entire process, from the time you arrive to the time you leave, takes about an hour and 15 min.<img src="images_imageMenu/blood_2.jpg" align="right"></li>
<li>The average adult has about 10 units of blood in his body. Roughly 1 unit is given during a donation.</li> 
<li>A healthy donor may donate red blood cells every 56 days, or double red cells every 112 days.</li>
<li>A healthy donor may donate platelets as few as 7 days apart, but a maximum of 24 times a year..</li>
<li>All donated blood is tested for HIV, hepatitis B and C, syphilis and other infectious diseases before it can be transfused to patients.</li>
</ul><hr>

<h4>Facts about blood and its components</h4>   
     <ul>         
<li>Blood makes up about 7 percent of your body's weight.<img src="images_imageMenu/blood_7.jpg" align="right"></li>
<li>There are four types of transfusable products that can be derived from blood: red cells, platelets, plasma and cryoprecipitate. Typically, two or three of these are produced from a unit of donated whole blood – hence each donation can help save up to three lives.</li>
<li>Donors can give either whole blood or specific blood components only. The process of donating specific blood components – red cells, plasma or platelets – is called apheresis.</li> 
<li>One transfusion dose of platelets can be obtained through one apheresis donation of platelets or by combining the platelets derived from five whole blood donations.</li>
<li>Donated platelets must be used within five days of collection.</li> 
<li>Healthy bone marrow makes a constant supply of red cells, plasma and platelets. The body will replenish the elements given during a blood donation – some in a matter of hours and others in a matter of weeks.</li>
</ul><hr>
