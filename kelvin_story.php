        <h1>Amazing Stories - Kelvin’s story</h1><hr>
        <p><img style="padding-right: 5px;" src="images_imageMenu/Kelvin-Bruce-Smith_large.jpg" align="left"> Kelvin was diagnosed with Leukaemia at 6 years of age, and after numerous tests and further bouts of illness, specialists delivered the devastating news that he would need a bone marrow transplant to save his life.</p><br>
        <p>However, with low numbers of suitable bone marrow donors, the odds were stacked against 6-year-old Kelvin Bruce-Smith.</p><br>
        <p>Without healthy bone marrow, Kelvin would need regular blood transfusions just to keep him alive.</p><br>
        <p>Unlike matching blood types, a person’s race does play a significant role in bone marrow matching. So the young battler from Te Rarawa, Te Aupouri, Ngatikahu and Ngahuhi-Nui-Tono iwi, needed a bone marrow donor of Maori descent who closely matched his tissue type. A match within Kelvin’s close family was not found, so the search was passed to the New Zealand Bone Marrow Registry.</p><br>
        <p>With only 6,000 Māori or Pacific Island donors on the NZ database (compared to the 11.5 million European donors on other registries), Kelvin was unable to find a bone marrow donor. This meant more waiting, during which time he received 22 units of platelets, 9 units of red blood cells, 9 units of fresh frozen plasma and 41 units of plasma derived blood products.</p><br>
        <p>Kelvin was unable to find a bone marrow donor anywhere in the world. Luckily, because of his age, Kelvin was able to receive a unit of umbilical cord blood (which also contains the necessary blood forming cells found in bone marrow) 5 months before his 7th birthday.</p><br>
        <p>Following his transplant, Kelvin was closely monitored with regular check-ups and was finally given the all clear in 2004. Today, Kelvin leads an active life like any other 12-year-old boy.</p>
