        <h1>Blood Types</h1><hr>
        <p>Although all blood is made of the same basic elements, not all blood is alike. 
            In fact, there are eight different common blood types, which are determined by the presence or absence of certain antigens – substances that can trigger an immune response if they are foreign to the body. 
            Since some antigens can trigger a patient's immune system to attack the transfused blood, safe blood transfusions depend on careful blood typing and cross-matching.</p>
        <h3>The ABO Blood Group System</h3><hr>
        <p>
            There are four major blood groups determined by the presence or absence of two antigens – A and B – on the surface of red blood cells:
        </p>
         
     <ul>         
<li><b>Group A</b> – has only the A antigen on red cells (and B antibody in the plasma)</li>
<li><b>Group B</b> – has only the B antigen on red cells (and A antibody in the plasma)</li>
<li><b>Group AB</b> – has both A and B antigens on red cells (but neither A nor B antibody in the plasma)</li> 
<li><b>Group O</b> – has neither A nor B antigens on red cells (but both A and B antibody are in the plasma)</li>
</ul>
