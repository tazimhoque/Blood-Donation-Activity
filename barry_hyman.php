        <h1>Amazing Stories - Barry Hyman</h1><hr>
        <p>Barry Hyman has been donating blood for more than 50 years. <img style="padding-right: 5px;" src="images_imageMenu/amazingstories_large_barry_hyman.jpg" align="left"> In this time, he has potentially saved the lives of more than 450 adults.</p><br>
        <p>In just over 50 years, Barry Hyman has donated enough blood to save the lives of more than 450 adults. The first time Barry gave blood he was in his early 20´s but due to his brother contracting haemolitic anaemia at a young age he already knew all about the need for blood and had experienced firsthand what a difference it can make.</p><br>
        <p>Mr Hyman said: "My brother was only 11 when he was diagnosed with haemolitic anaemia. He needed to have his spleen removed and was given a few pints of blood. Following that my father started giving blood almost instantly and carried on donating for as long as he could, my brother is now a healthy grandfather."</p><br>
        <p>In addition to giving blood himself, Mr Hyman is also more than happy to persuade others to donate too.</p><br>
        <p>"I often talk to people about giving blood, the system is brilliant, the staff are wonderful and it costs you hardly anything in time and energy. After I gave blood once and thought about how essential it is that people like me carry on donating, I realised that there was no good reason to stop. As long as I am healthy, I will carry on giving blood."</p><br>
