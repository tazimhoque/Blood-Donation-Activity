        <h1>Donation FAQs</h1><hr>
        <h4>Donation Process</h4><hr>
         <font color="blue">What should I do after donating blood?</font>    
     <ul>         
<li>Take the following precautions:
    <br>– Drink an extra four glasses (eight ounces each) of non-alcoholic liquids.
    <br>– Keep your bandage on and dry for the next five hours, and do not do heavy exercising or lifting.
    <br>– If the needle site starts to bleed, raise your arm straight up and press on the site until the bleeding stops.
    <br>– Eat healthy meals and consider adding iron-rich foods to your regular diet, or discuss taking an iron supplement with your health care provider, to replace the iron lost with blood donation.</li>
    
<li><b>If you get a bruise:</b>  Apply ice to the area intermittently for 10-15 minutes during the first 24 hours. Thereafter, apply warm, moist heat to the area intermittently for 10-15 minutes. A rainbow of colors may occur for about 10 days.</li>
<li><b>If you get dizzy or lightheaded:</b>  Stop what you are doing, lie down, and raise your feet until the feeling passes and you feel well enough to safely resume activities.</li> 
<li>Schedule your next appointment.</li>
</ul>
 <font color="blue">How long does a blood donation take?</font>
 <p>
     The entire process takes about one hour and 15 minutes; the actual donation of a pint of whole blood unit takes eight to 10 minutes. However, the time varies slightly with each person depending on several factors including the donor’s health history and attendance at the blood drive.
 </p>
 <font color="blue">How long will it take to replenish the pint of blood I donate?</font> 
 <p>The plasma from your donation is replaced within about 24 hours. Red cells need about four to six weeks for complete replacement. That’s why at least eight weeks are required between whole blood donations.
</p>
<hr>
<h4>Donation Process</h4><hr>
 <font color="blue">How often can I donate blood?</font>
 <p>You must wait at least eight weeks (56 days) between donations of whole blood and 16 weeks (112 days) between double red cell donations. Platelet apheresis donors may give every 7 days up to 24 times per year. Regulations are different for those giving blood for themselves (autologous donors).</p>
 <font color="blue">How often can I donate blood?</font>
 <p>
     In most states, donors must be age 17 or older. Some states allow donation by 16-year-olds with a signed parental consent form. Donors must weigh at least 110 pounds and be in good health. Additional eligibility criteria apply.
 </p>
